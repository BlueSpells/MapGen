// CSearchDlg dialog

CSearchDlg::CSearchDlg(CWnd* pParent /*=NULL*/)
	: CDialog(CSearchDlg::IDD, pParent)
    , m_pParent(pParent)
    , m_FindTerm(_T(""))
    , m_CaseSensitive(FALSE)
{
}

CSearchDlg::~CSearchDlg()
{
}

bool CSearchDlg::Init()
{
    return CreateIndirect((LPCDLGTEMPLATE)SearchDialog, m_pParent) != 0;
}

void CSearchDlg::Show()
{
    m_FindTermCtrl.SetFocus();
    ShowWindow(SW_SHOW);
}

BOOL CSearchDlg::OnInitDialog()
{
    CDialog::OnInitDialog();

    CenterWindow();
    return TRUE;
}

void CSearchDlg::DoDataExchange(CDataExchange* pDX)
{
    CDialog::DoDataExchange(pDX);
    DDX_Text(pDX, IDC_FIND_TERM, m_FindTerm);
    DDX_Control(pDX, IDC_FIND_TERM, m_FindTermCtrl);
    DDX_Check(pDX, IDC_CASE_SENSITIVE_CHECK, m_CaseSensitive);
}

void CSearchDlg::OnOK()
{
    UpdateData(TRUE);
    m_FindTermCtrl.UpdateLru();
    ::PostMessage(m_pParent->m_hWnd, WM_SEARCH_REQUEST, NULL, NULL);
}


